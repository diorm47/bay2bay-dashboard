export function namePage(path) {
  if (path == "/team") {
    return "Команда сайта";
  } else if (path == "/shops") {
    return "Магазины";
  }
  if (path == "/ads") {
    return " Реклама и связь";
  }
  if (path == "/prefix") {
    return "Префиксы тем";
  }
  if (path == "/icons-banners") {
    return "Значки и баннеры";
  }
  if (path == "/users") {
    return "Пользователи";
  }
  if (path == "/nodes") {
    return "Узлы";
  }
  if (path == "/settings") {
    return "Настройки";
  }
  if (path == "/") {
    return "Настройки";
  }
  if (path == "/user-level") {
    return "Уровни пользователей";
  }
}
